    private void calculate_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculate_btnActionPerformed
        // TODO add your handling code here:
        // Inputs
        String s_l = arraival_rate.getText();
        String s_m = service_rate.getText();
        String s_t = t_txt1.getText();
        String s_k = k_txt.getText();
        String s_n = nofcustomer_txt2.getText();
        String s_M = M_txt3.getText();

        double n_st = Double.parseDouble(s_t);
        double n_sk = Double.parseDouble(s_k);
        double n_sn = Double.parseDouble(s_n);
        double n_sM = Double.parseDouble(s_M);

        if (!s_l.contains("/") && !s_l.contains(".")) {
            lmda = Double.parseDouble(s_l);
        }
        if (!s_m.contains("/") && !s_m.contains(".")) {
            mue = Double.parseDouble(s_m);
        } else {
            Inputs in = new Inputs();
            lmda = in.ConvertInput(s_l);
            mue = in.ConvertInput(s_m);
        }
        if ((lmda == 0 || lmda < 0) || (mue == 0 || mue < 0) || (n_st == 0 || n_st < 0) || (n_sk == 0 || n_sk < 0) || (n_sn == 0 || n_sn < 0) || (n_sM < 0)) {
            Invalid_Inputs n = new Invalid_Inputs();
            n.setVisible(true);
            n.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            n.setLocationRelativeTo(null);
        } else {

            k = n_sk;
            t = n_st;
            n = n_sn;
            m = n_sM;
            noft = Math.floor((lmda * t) - ((mue * t) - (mue / lmda)));

            // Case 1 : λ > μ
            if (lmda > mue) {
                int ti = (int) ((k - (mue / lmda)) / (lmda - mue));
                int i;
                // trial  and error
                for (i = 0; i < ti; i++) {
                    if (k == Math.floor(i / (1 / lmda)) - Math.floor((i / (1 / mue)) - ((1 / lmda) / (1 / mue)))) {
                        break;
                    }
                }
                ti = i;
                res_ti = Integer.toString(ti);
                ti_txt.setText("By trial and error, ti = " + res_ti);

                if (1 / lmda % 1 / mue == 0) {
                    if (t < 1 / lmda) {
                        nt1_txt1.setText("t<1/λ   so  n(t) = 0");
                    } else if (t > 1 / lmda && t <= ti) {
                        nt1_txt1.setText("n(t)=|λti|-|μti-μ/λ| = " + noft);
                    } else {
                        nt1_txt1.setText("n(t)= k-1 = " + (k - 1));
                    }
                } else {
                    if (t < 1 / lmda) {
                        nt1_txt1.setText("t<1/λ   so  n(t) = 0");
                    } else if (t > 1 / lmda && t <= ti) {
                        nt1_txt1.setText("n(t)=|λti|-|μti-μ/λ| = " + noft);
                    }
                }
                if (n < lmda * ti) {
                    wq_txt3.setText("n<λti   so Wq(n) = " + ((1 / mue) - (1 / lmda)) * (n - 1));
                } else {
                    wq_txt3.setText("n>=λti   so Wq(n) = " + ((1 / mue) - (1 / lmda)) * (lmda * ti - 2));
                }

            } // Case 2 : μ >= λ
            else {
                if (lmda == mue) {
                    ti_txt.setText("μ=λ so   ti = 0");
                    nt1_txt1.setText("μ=λ so  n(t) = " + m);
                    wq_txt3.setText("μ=λ so  Wq(n) = (M-1)(1/μ) = " + ((m - 1) * (1 / mue)));
                }
                if (lmda < mue) {
                    int ti = (int) (m / (mue - lmda));
                    int i;
                    for (i = 0; i < ti; i++) {
                        if (m == Math.floor(i / (1 / mue)) - Math.floor(i / (1 / mue))) {
                            break;
                        }
                    }
                    ti = i;
                    res_ti2 = Integer.toString(ti);
                    ti_txt.setText("By trial and error, ti = " + res_ti2);
                    if (t < ti) {
                        nt1_txt1.setText("t<ti so n(t)=M+|λt|-|μt|  = " + ((m + Math.floor(lmda * t) - Math.floor(mue * t))));
                    } else {
                        nt1_txt1.setText("t>=ti so  n(t) = 0 or 1 ");
                    }
                    if (n == 0) {
                        wq_txt3.setText("((M-1)/2μ)= " + (int) Math.floor((m - 1) / (2 * mue)));
                    } else if (n < (int) Math.floor(lmda * ti)) {
                        wq_txt3.setText("Wq(n) = (M-1+n)(1/μ)-(n/λ) = " + (((m - 1 + n) * ((1 / mue)) - (n * 1 / lmda))));
                    } else {
                        wq_txt3.setText(" n<=λti  so  Wq(n) = 0");
                    }
                }

            }
        }
    }//GEN-LAST:event_calculate_btnActionPerformed

