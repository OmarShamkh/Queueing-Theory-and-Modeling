    private void graph_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_graph_btnActionPerformed
        // TODO add your handling code here:
        String s_l = arraival_rate.getText();
        String s_m = service_rate.getText();
        String s_M = M_txt3.getText();
        String s_k = k_txt.getText();
        double n_sM = Double.parseDouble(s_M);
        double n_sk = Double.parseDouble(s_k);
        k = n_sk;
        m = n_sM;
        Inputs in = new Inputs();
        lmda = in.ConvertInput(s_l);
        mue = in.ConvertInput(s_m);

        //Graph
        XYSeriesCollection dataset = new XYSeriesCollection();
        XYSeries series1 = new XYSeries("D/D/1/K-1");

        if (lmda > mue) {
            for (int i = 0; i <= Integer.parseInt(res_ti) + 10; i++) {
                if (1 / lmda % 1 / mue == 0) {
                    if (i <= 1 / lmda) {
                        series1.add(i, 0);
                    } else if (i > 1 / lmda && i <= Integer.parseInt(res_ti)) {
                        series1.add(i, Math.floor((lmda * i) - ((mue * i) - (mue / lmda))));
                    } else {
                        series1.add(i, k - 1);
                    }
                } else {
                    if (i < 1 / lmda) {
                        series1.add(i, 0);
                    } else if (i >= 1 / lmda && i < Integer.parseInt(res_ti)) {
                        series1.add(i, Math.floor((lmda * i) - ((mue * i) - (mue / lmda))));
                    }
                }
            }

        } else {
            for (int i = 0; i <= Integer.parseInt(res_ti2) + 10; i++) {
                if (lmda < mue) {
                    if (i < Integer.parseInt(res_ti2)) {
                        series1.add(i, (m + Math.floor(lmda * i) - Math.floor(mue * i)));
                    } else {
                        for (int j = i; j <= i + (1 / mue); j++) {
                            series1.add(j, 0);
                        }
                        i += 1 / mue + 1;
                        series1.add(i, 1);

                    }
                }

            }
        }

        dataset.addSeries(series1);
        JFreeChart chart = ChartFactory.createXYStepAreaChart(
                "Graph Queuery", // Chart Title
                "Time t ", // X-Axis Label
                "Number of customers", // Y-Axis Label
                dataset);

        XYPlot plot = (XYPlot) chart.getPlot();
        plot.setBackgroundPaint(new Color(229, 150, 97, 60));

        ChartPanel panel = new ChartPanel(chart);
        setContentPane(panel);
        this.setVisible(true);


    }//GEN-LAST:event_graph_btnActionPerformed

